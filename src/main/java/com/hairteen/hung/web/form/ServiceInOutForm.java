package com.hairteen.hung.web.form;

public class ServiceInOutForm {

    private Integer idService;
    private String nameService;
    private String countInOut;
    private String resonInOut;
    private Integer statusInOut;
    public Integer getIdService() {
        return idService;
    }
    public void setIdService(Integer idService) {
        this.idService = idService;
    }
    public String getNameService() {
        return nameService;
    }
    public void setNameService(String nameService) {
        this.nameService = nameService;
    }
    public String getCountInOut() {
        return countInOut;
    }
    public void setCountInOut(String countInOut) {
        this.countInOut = countInOut;
    }
    public String getResonInOut() {
        return resonInOut;
    }
    public void setResonInOut(String resonInOut) {
        this.resonInOut = resonInOut;
    }
    public Integer getStatusInOut() {
        return statusInOut;
    }
    public void setStatusInOut(Integer statusInOut) {
        this.statusInOut = statusInOut;
    }
}
